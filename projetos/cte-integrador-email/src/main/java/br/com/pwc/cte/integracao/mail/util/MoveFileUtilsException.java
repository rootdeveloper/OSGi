public class MoveFileUtilsException extends Exception {

	private static final long serialVersionUID = -4520546732593192124L;

	
	public MoveFileUtilsException() {
		super();
	}

	public MoveFileUtilsException(String message, Throwable cause) {
		super(message, cause);
	}

	public MoveFileUtilsException(String message) {
		super(message);
	}

	public MoveFileUtilsException(Throwable cause) {
		super(cause);
	}

}
